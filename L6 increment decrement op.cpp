//L6 increment decrement op

#include <iostream>

using namespace std;

int main()
{  int a=5;
    //a=a+1 == a++;
    //a++ //post increment 
    //++a //pre decrement 
    cout<<++a<<endl;
     //a=5;
    cout<<a++<<endl;

 
    cout<<a--<<endl;
     //a=5;
    cout<<--a<<endl;
    
   // a=a+3 == a+=3;
    
    // a+b*c  
    int b= 10*5/9;
    cout<<b<<endl;
    
    
    return 0;
}
